public class Bullet {
    private double x;
    private double y;
    private double aimX;
    private double aimY;
    private int r;

    Bullet(double x, double y, double aimX, double aimY, int r) {
        this.x = x;
        this.y = y;
        this.aimX = aimX;
        this.aimY = aimY;
        this.r = r;
    }
}
